// Helper für lesbarere Tafel-Definitionen
function s(
  kostenEur: number,
  zoneI: [number, number],
  zoneII: [number, number],
  zoneIII: [number, number],
  zoneIV: [number, number],
  zoneV: [number, number]
): Stuetzstelle {
  return {
    kostenCents: kostenEur * 100,
    zoneI: { minCents: zoneI[0] * 100, maxCents: zoneI[1] * 100 },
    zoneII: { minCents: zoneII[0] * 100, maxCents: zoneII[1] * 100 },
    zoneIII: { minCents: zoneIII[0] * 100, maxCents: zoneIII[1] * 100 },
    zoneIV: { minCents: zoneIV[0] * 100, maxCents: zoneIV[1] * 100 },
    zoneV: { minCents: zoneV[0] * 100, maxCents: zoneV[1] * 100 },
  };
}

